import Head from "next/head";
import { getAnimes } from "../../api";
import AnimeCard from "../../components/AnimeCard";
import { Grid, Heading, Container } from "../../components/styled";

const Home = ({ animes, pageInfo }) => {
  return (
    <>
      <Head>
        <title>Anilist Collection</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <Heading>Explore Anime</Heading>

      <Container>
        <Grid>
          {animes.map(
            ({
              id,
              title,
              description,
              episodes,
              coverImage,
              averageScore,
            }) => (
              <AnimeCard
                key={id}
                id={id}
                title={title}
                description={description}
                episodes={episodes}
                coverImage={coverImage}
                averageScore={averageScore}
              />
            )
          )}
        </Grid>
      </Container>
    </>
  );
};

export default Home;

export async function getServerSideProps(context) {
  const { page } = context.query;

  const { data } = await getAnimes(page || 1, 10);

  const animes = data.Page.media;
  const pageInfo = data.Page.pageInfo;
  return {
    props: { animes, pageInfo },
  };
}
